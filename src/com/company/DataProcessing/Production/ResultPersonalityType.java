package com.company.DataProcessing.Production;

public class ResultPersonalityType {
    float dim1f, dim2f, dim3f, dim4f;

    public ResultPersonalityType(float dim1f, float dim2f, float dim3f, float dim4f) {
        this.dim1f = dim1f;
        this.dim2f = dim2f;
        this.dim3f = dim3f;
        this.dim4f = dim4f;
    }

    public float getDim1f() {
        return dim1f;
    }

    public float getDim2f() {
        return dim2f;
    }

    public float getDim3f() {
        return dim3f;
    }

    public float getDim4f() {
        return dim4f;
    }

}
